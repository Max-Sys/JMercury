    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()) {
            return;
        }
        String srv = Vars.prop.getProperty("ServerAddr");
        String port = Vars.prop.getProperty("ServerPort");

        Vars.prop.setProperty("ServerAddr", jTextField1.getText());
        Vars.prop.setProperty("ServerPort", jTextField2.getText());

        String status = NetClient.sendGetServerStatus();
        if (!status.isEmpty()) {
            JOptionPane.showMessageDialog(this, status);
            Vars.SaveProperties();
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Невозможно подключиться к серверу!");
            if (srv != null) {
                Vars.prop.setProperty("ServerAddr", srv);
            }
            if (port != null) {
                Vars.prop.setProperty("ServerPort", port);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

